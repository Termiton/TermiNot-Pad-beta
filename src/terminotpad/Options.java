    public Options() {
        FileReader reader = null;
        String dbtype = null;          // = "sqlserver";
        String dbserv = null;          // = "BLAISE-PC";
        long dbport = 1;                // = 1433;
        String dbname = null;          // = "sqlsupport.dbo";
        String usrnam = null;          // = "usersupport";
        String psswrd = null;          // = "azerty";
        try {
            initComponents();
            String filePath = "src/doc/config.json";
            // read the json file
            reader = new FileReader(filePath);
            JSONParser jsonParser = new JSONParser();
            JSONObject jsonObject = null;
            try {
                jsonObject = (JSONObject) jsonParser.parse(reader);
            } catch (IOException ex) {
                Logger.getLogger(Options.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ParseException ex) {
                Logger.getLogger(Options.class.getName()).log(Level.SEVERE, null, ex);
            }
            // get a String from the JSON object
            dbtype = (String) jsonObject.get("dbtype");
            dbserv = (String) jsonObject.get("dbserv");
            dbname = (String) jsonObject.get("dbname");
            usrnam = (String) jsonObject.get("usrnam");
            psswrd = (String) jsonObject.get("psswrd");
            // get a number from the JSON object
            dbport = (long) jsonObject.get("dbport");
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Options.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                reader.close();
            } catch (IOException ex) {
                Logger.getLogger(Options.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        try {
            // chargement de la classe du pilote
            switch (dbtype) {
                case "sqlserver":
                    jRadioButtonMSSQL.setSelected(true);
                    jRadioButtonMYSQL.setSelected(false);
                    break;
                case "mysql":
                    jRadioButtonMYSQL.setSelected(true);
                    jRadioButtonMSSQL.setSelected(false);
                default:
                    /**/
                    System.out.println("Pas de base de donnée valide sélectionnée");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        jTextFieldSrvName.setText(dbserv);
        jTextFieldbdd.setText(dbname);
        jTextFielduser.setText(usrnam);
        jTextFieldpwd.setText(psswrd);
        jTextFieldsrvPort.setText(Long.toString(dbport));

    }

