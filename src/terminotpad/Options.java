    private void jButtonRecordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRecordActionPerformed
        // TODO add your handling code here:
        String dbtype;
        String srvname;
        String srvport;
        String dbname;
        String user;
        String pwd;

        if (jRadioButtonMSSQL.isSelected()) {
            dbtype = "sqlserver";
        } else if (jRadioButtonMYSQL.isSelected()) {
            dbtype = "mysql";
        } else {
            dbtype = "mysql";
        }
        srvname = jTextFieldSrvName.getText();
        srvport = jTextFieldsrvPort.getText();
        dbname = jTextFieldbdd.getText();
        user = jTextFielduser.getText();
        pwd = jTextFieldpwd.getText();

        String cheminDuFichier = "src/doc/config.json";

        String jsonContent = "{"
                + "\"dbtype\":\"" + dbtype + "\""
                + ",\"dbserv\":\"" + srvname + "\""
                + ",\"dbport\":" + srvport + ""
                + ",\"dbname\":\"" + dbname + "\""
                + ",\"usrnam\":\"" + user + "\""
                + ",\"psswrd\":\"" + pwd + "\""
                + "}";
        
        Fichier file = new Fichier(cheminDuFichier, jsonContent);
        file.fileWrite();

        this.setVisible(false);
    }//GEN-LAST:event_jButtonRecordActionPerformed

